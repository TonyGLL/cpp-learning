#include <iostream>
#include <string>

using namespace std;

int main()
{
    // 1. Integers (numbers without decimals)
    int age = 25;

    // 2. Floats (numbers with decimals)
    double height = 1.75;

    // 3. Characters (a single letter, use single quotes)
    char initial = 'J';

    // 4. Booleans (True/False)
    bool isStudent = true;

    // 5. Strings (Text, use double quote)
    string name = "Tony";

    cout << "Name: " << name << endl;
    cout << "Age: " << age << endl;
    cout << "Height: " << height << endl;

    return 0;
}